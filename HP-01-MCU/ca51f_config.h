
#include "ca51f3sfr.h"

#define IRCH		0
#define IRCL		1
#define PLL			2
#define XOSCH		3
#define XOSCL		4

/**************************************************************************************************************/
#define SYSCLK_SRC	PLL									//芯片系统时钟选择,一般情况设置为IRCH或PLL
/**************************************************************************************************************/

/************系统时钟频率定义，主要用于配置UART波特率**********************************************************/
#if (SYSCLK_SRC == IRCH)
	#define FOSC		(3686400)
#elif (SYSCLK_SRC == PLL)
	#define PLL_Multiple			7			//定义PLL倍频倍数,当IRCH的频率为3.6864M时，最高倍频数不要超过6倍
	#define FOSC		(3686400*PLL_Multiple)
#else
	#define FOSC		(3686400)
#endif


#define INT_TIME			1000			//定时时间，单位为us

#define	TH_VAL				(unsigned char)((0x10000 - (INT_TIME*(FOSC/1000))/12000)>>8)
#define	TL_VAL				(unsigned char)(0x10000 - (INT_TIME*(FOSC/1000))/12000)



/***************************************************************************************************************/

#define RTC_CLK_SELECT		IRCL			//定义RTC时钟源， 在外接32768晶振时定义为XOSCL，否则定义为IRCL
/***************************************************************************************************************/

